#include "libft.h"

char	*ft_strcpy(char *dst, char const *src)
{
	int	a;

	a = 0;
	while (src[a] != '\0')
	{
		dst[a] = src[a];
		a++;
	}
	dst[a] = '\0';
	return (dst);
}
